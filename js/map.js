  //helpers
  function toIndex(x,y) {
    return ((y*mapW) + x);
  }




  //listeners
  window.onload = function() {
    const game = document.getElementById("game")
    ctx = game.getContext('2d');
    requestAnimationFrame(drawGame);
    ctx.font = "bold 10pt sans-serif";

    window.addEventListener("keydown", (e) => {
      if (e.keyCode >= 37 && e.keyCode <= 40) {
        keysDown[e.keyCode] = true;
      }
    });
    window.addEventListener("keyup", (e) => {
      if (e.keyCode >= 37 && e.keyCode <= 40) {
        keysDown[e.keyCode] = false;
      }
    });
    //this is setting the viewport screen area 
    viewport.screen = [game.width, game.height]
  };

  function drawGame() {
    if(ctx === null) return;
    
    const currentFrameTime = Date.now()
    const timeElapsed = currentFrameTime - lastFrameTime

    let sec = Math.floor(Date.now()/1000);
    if (sec !== currentSecond) {
      currentSecond = sec;
      framesLastSecond = frameCount;
      frameCount = 1
    } else {
      frameCount ++
    }
    //Player movement//////////////////////////////////////////////////////
    if (!player.processMovement(currentFrameTime)) {
      function inBounds() {
        const x = player.tileFrom[0]
        const y = player.tileFrom[1]
        return (x > 0 && x < (mapW-1)) && (y > 0 && y < mapH)
      }
      if (inBounds()) {
        //Left + up arrow
        if ((keysDown[37] && keysDown[38]) 
            && gameMap[toIndex((player.tileFrom[0] - 1), player.tileFrom[1] - 1)] !== 1) {
              player.tileTo[0] -= 1
              player.tileTo[1] -= 1
        }
        //Left + down arrow
        else if ((keysDown[37] && keysDown[40]) 
            && gameMap[toIndex((player.tileFrom[0] - 1), player.tileFrom[1] + 1)] !== 1) {
            player.tileTo[0] -= 1
            player.tileTo[1] += 1
            
        }
        //right + up arrow
        else if ((keysDown[39] && keysDown[38]) 
            && gameMap[toIndex((player.tileFrom[0] + 1), player.tileFrom[1] - 1)] !== 1) {
              player.tileTo[0] += 1
              player.tileTo[1] -= 1
        }
        //right + down arrow
        else if ((keysDown[39] && keysDown[40]) 
            && gameMap[toIndex((player.tileFrom[0] + 1), player.tileFrom[1] + 1)] !== 1) {
            player.tileTo[0] += 1
            player.tileTo[1] += 1
        }

        else if (keysDown[38] 
              //checking that player current of y is > the edge of the board
              //checking that the index of the map at current location x, y-1 != wall
              && gameMap[toIndex(player.tileFrom[0], (player.tileFrom[1] - 1))] !== 1) {
                player.tileTo[1] -= 1
                
          }
          //Down arrow
          else if (keysDown[40] 
              //checking that the index of the map at current location x, y+1 != wall
              && gameMap[toIndex(player.tileFrom[0], (player.tileFrom[1] + 1))] !== 1) {
                player.tileTo[1] += 1
          }
          //Left arrow
          else if (keysDown[37] 
              //tile from x - 1 (moving left), y is not a wall
              && gameMap[toIndex((player.tileFrom[0] - 1), player.tileFrom[1])] !== 1) {
                player.tileTo[0] -= 1
          }
          //right arrow
          else if (keysDown[39] 
              //tileFrom x value < the edge of the board
              && gameMap[toIndex((player.tileFrom[0] + 1), player.tileFrom[1])] !== 1) {
                player.tileTo[0] += 1
          }
    }
      
      //check to see if player is moving
      if((player.tileFrom[0] !== player.tileTo[0]) || (player.tileFrom[1] !== player.tileTo[1])){
        player.timeMoved = currentFrameTime
      }
      
    }
   ////////////////////////////////////////////////////////////////


    viewport.update(player.position[0] + (player.dimensions[0]/2), player.position[1] + (player.dimensions[1]/2))
    
    ctx.fillStyle = "#000000";
    ctx.fillRect(0, 0, viewport.screen[0], viewport.screen[1]);

    
    
    
    //draw from map
    for(let y = viewport.startTile[1]; y <= viewport.endTile[1]; y++) {
      for(let x = viewport.startTile[0]; x <= viewport.endTile[0]; x++) {
        switch(gameMap[(y * mapW) + x]) {
          case 0:
            ctx.fillStyle = "#999";
            break;
          default: 
            ctx.fillStyle = "#eee";
        }
        //render tile
       ctx.fillRect( (viewport.offset[0] + (x*tileW)), ( viewport.offset[1] + (y*tileH)), tileW, tileH);

      }
    }



  //render player
  ctx.fillStyle = "#0000ff";
  ctx.fillRect((viewport.offset[0] + player.position[0]), (viewport.offset[1] + player.position[1]), player.dimensions[0], player.dimensions[1]);
  
  ctx.fillStyle = "ff0000";
  ctx.fillText("FPS: " + framesLastSecond, 10, 20)
  
  lastFrameTime = currentFrameTime;
  requestAnimationFrame(drawGame)
}


