  //helpers
  function toIndex(x,y) {
    return ((y*mapW) + x);
  }

  function validator() {
    x = player.tileFrom[0]
    y = player.tileFrom[0]

    return (x > 0 && x < (mapW-1)) && (y > 0 && y < mapH)
  }


  //listeners
  window.onload = function() {
    ctx = document.getElementById('game').getContext('2d');
    requestAnimationFrame(drawGame);
    ctx.font = "bold 10pt sans-serif";

    window.addEventListener("keydown", (e) => {
      if (e.keyCode >= 37 && e.keyCode <= 40) {
        keysDown[e.keyCode] = true;
      }
    });
    window.addEventListener("keyup", (e) => {
      if (e.keyCode >= 37 && e.keyCode <= 40) {
        keysDown[e.keyCode] = false;
      }
    });
  };

  function drawGame() {
    if(ctx === null) return;
    
    const currentFrameTime = Date.now()
    const timeElapsed = currentFrameTime - lastFrameTime

    let sec = Math.floor(Date.now()/1000);
    if (sec !== currentSecond) {
      currentSecond = sec;
      framesLastSecond = frameCount;
      frameCount = 1
    } else {
      frameCount ++
    }
    //Player movement
    if (!player.processMovement(currentFrameTime)) {
            //////////////////////////////////
      if (validator())
            //Left + up arrow
            if ((keysDown[37] && keysDown[38]) 
                && (player.tileFrom[0] > 0 && player.tileFrom[1] > 0) 
                && gameMap[toIndex((player.tileFrom[0] - 1), player.tileFrom[1]-1)] !== 1) {
                  player.tileTo[0] -= 1
                  player.tileTo[1] -= 1
            }
            //Left + down arrow
            // else if ((keysDown[37] && keysDown[40]) 
            //     && player.tileFrom[0] > 0 && player.tileFrom[1] < (mapH-1)
            //     && gameMap[toIndex((player.tileFrom[0] - 1), player.tileFrom[1] + 1)] !== 1)
            //      {
            //       console.log("LEFT + DOWN")
      
            //       // player.tileTo[0] -= 1
            // }

      //if the key is pressed and the player is within bounds and that the Up arrow will not 
      //put a character into a wall (-1 because we are moveing close to index 0)
      
     else if (keysDown[38] 
          //checking that player current of y is > the edge of the board
          && validator()
          //checking that the index of the map at current location x, y-1 != wall
          && gameMap[toIndex(player.tileFrom[0], (player.tileFrom[1] - 1))] !== 1) {
            player.tileTo[1] -= 1
            
      }
      //Down arrow
      else if (keysDown[40] 
          //checking that player current of y is < the edge of the board
          && player.tileFrom[1] < (mapH-1) 
          //checking that the index of the map at current location x, y+1 != wall
          && gameMap[toIndex(player.tileFrom[0], (player.tileFrom[1] + 1))] !== 1) {
            player.tileTo[1] += 1
      }
      //Left arrow
      else if (keysDown[37] 
          //tileFrom x value > the edge of the board
          && player.tileFrom[0] > 0 
          //tile from x - 1 (moving left), y is not a wall
          && gameMap[toIndex((player.tileFrom[0] - 1), player.tileFrom[1])] !== 1) {
            player.tileTo[0] -= 1
      }
      //right arrow
      else if (keysDown[39] 
          //tileFrom x value < the edge of the board
          && player.tileFrom[0] < (mapW-1) 
          //tile from x + 1 (moving right), y is not a wall
          && gameMap[toIndex((player.tileFrom[0] + 1), player.tileFrom[1])] !== 1) {
            player.tileTo[0] += 1
      }



      //right arrow
      // if (keysDown[39] 
      //     //tileFrom x value < the edge of the board
      //     && player.tileFrom[0] < (mapW-1) 
      //     //tile from x + 1 (moving right), y is not a wall
      //     && gameMap[toIndex((player.tileFrom[0] + 1), player.tileFrom[1])] !== 1) {
      //       player.tileTo[0] += 1
      // }
      







































      //check to see if player is moving
      if((player.tileFrom[0] !== player.tileTo[0]) || (player.tileFrom[1] !== player.tileTo[1])){
        player.timeMoved = currentFrameTime
      }
      
    }
    
    //draw from map
    for(let row = 0; row < mapH; row++) {
      for(let col = 0; col < mapW; col++) {
        switch(gameMap[(row * mapW) + col]) {
          case 0:
            ctx.fillStyle = "#999";
            break;
          default: 
            ctx.fillStyle = "#eee";
        }
        //render tile
        ctx.fillRect(col * tileW, row * tileH, tileW, tileH);
      }
    }

  //render player
  ctx.fillStyle = "#0000ff";
  ctx.fillRect(player.position[0], player.position[1], player.dimensions[0], player.dimensions[1]);
  
  ctx.fillStyle = "ff0000";
  ctx.fillText("FPS: " + framesLastSecond, 10, 20)
  
  lastFrameTime = currentFrameTime;
  requestAnimationFrame(drawGame)
}